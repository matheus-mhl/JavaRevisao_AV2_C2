/*Uma classe abstrata é um tipo especial de classe em Java usada como modelo para outras classes. 
 * Ela não pode ser instanciada diretamente — ou seja, você não pode criar objetos a partir dela — mas pode conter características e comportamentos que 
 * serão compartilhados entre suas subclasses.*/
abstract class Forma { //Criando classe abstrata
	public abstract void desenhar(); //Criando Método abstrato. LEMBRE-SE método abstrato NAO POSSUI CORPO (não usa chaves {})

}
